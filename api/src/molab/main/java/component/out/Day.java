package molab.main.java.component.out;

public class Day {

	private Integer total;
	private Double percent;
	private Double rate;
	private Double stay;
	private Integer new_avg;
	private Integer old_avg;

	/**
	 * @return the total
	 */
	public Integer getTotal() {
		return total;
	}

	/**
	 * @param total
	 *            the total to set
	 */
	public void setTotal(Integer total) {
		this.total = total;
	}

	/**
	 * @return the percent
	 */
	public Double getPercent() {
		return percent;
	}

	/**
	 * @param percent
	 *            the percent to set
	 */
	public void setPercent(Double percent) {
		this.percent = percent;
	}

	/**
	 * @return the rate
	 */
	public Double getRate() {
		return rate;
	}

	/**
	 * @param rate
	 *            the rate to set
	 */
	public void setRate(Double rate) {
		this.rate = rate;
	}

	/**
	 * @return the stay
	 */
	public Double getStay() {
		return stay;
	}

	/**
	 * @param stay
	 *            the stay to set
	 */
	public void setStay(Double stay) {
		this.stay = stay;
	}

	/**
	 * @return the new_avg
	 */
	public Integer getNew_avg() {
		return new_avg;
	}

	/**
	 * @param new_avg
	 *            the new_avg to set
	 */
	public void setNew_avg(Integer new_avg) {
		this.new_avg = new_avg;
	}

	/**
	 * @return the old_avg
	 */
	public Integer getOld_avg() {
		return old_avg;
	}

	/**
	 * @param old_avg
	 *            the old_avg to set
	 */
	public void setOld_avg(Integer old_avg) {
		this.old_avg = old_avg;
	}

}
